function restore(html) {
  let result = html;
  for (const [from, to] of replacements) {
    if (from) result = result.split(from).join(to);
  }
  // Clean up empty class attributes and double spaces in classes
  result = result.replace(/class="\s+"/g, '');
  result = result.replace(/class="([^"]*)"/g, (_, cls) => {
    const cleaned = cls.replace(/\s+/g, ' ').trim();
    return cleaned ? `class="${cleaned}"` : '';
  });
  // Fix client marquee script
  result = result.replace(
    /function createClientCard\(name, hoverClass\) \{[\s\S]*?\}/,
    `function createClientCard(name, gradient) {
      return '<div class="flex-shrink-0 glass px-8 py-6 rounded-xl border border-slate-800 hover:border-slate-700 transition-colors group"><div class="flex items-center gap-3"><div class="w-10 h-10 rounded-lg bg-gradient-to-br from-slate-700 to-slate-800 flex items-center justify-center ' + gradient + ' transition-all duration-300"><span class="text-sm font-bold text-white">' + name.charAt(0) + '</span></div><span class="text-lg font-semibold text-slate-300 group-hover:text-white transition-colors whitespace-nowrap">' + name + '</span></div></div>';
    }`
  );
  result = result.replace("createClientCard(c, 'sym-client-icon-blue')", "createClientCard(c, 'group-hover:from-blue-600 group-hover:to-purple-600')");
  result = result.replace("createClientCard(c, 'sym-client-icon-purple')", "createClientCard(c, 'group-hover:from-purple-600 group-hover:to-pink-600')");
  // Remove bootstrap-custom.css link - use bootstrap-compat instead
  result = result.replace(
    '<link rel="stylesheet" href="css/bootstrap-custom.css">\n',
    '<link rel="stylesheet" href="css/bootstrap-compat.css">\n'
  );
  return result;
}

['index.html', 'about.html', 'contact.html'].forEach((f) => {
  const fp = path.join(dir, f);
  let html = fs.readFileSync(fp, 'utf8');
  html = restore(html);
  fs.writeFileSync(fp, html, 'utf8');
  console.log('Restored', f);
});
